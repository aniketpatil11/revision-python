'''
Script that converts following strings to lowercase: 
Animal, Badger, Honey Bee, Honeybadger
'''

print("Animal".lower())
print("Badger".lower())
print("Honey Bee".lower())
print("HoneyBadger".lower())


# Convert same words to uppercase

print("Animal".upper())
print("Badger".upper())
print("Honey Bee".upper())
print("HoneyBadger".upper())
